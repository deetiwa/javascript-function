// Add Two Numbers using a function

function add(a, b)                 //function is declared with two parameters a and b.
{
    console.log(a + b);            // print on console a+b
}
add(3,4);                          // pass argument/value
add(2,9);

// Sub two number using a function

function sub (x, y)
{
    console.log(x-y);
}
sub(8,5);                  // output 3
sub();                     // output NaN, because not given value

// mul two number using function

function mul ( c,d)
{
    console.log(c*d);
}
mul(8,);                    // output NaN, only one value pass 
mul(8,4);                   // output 32
